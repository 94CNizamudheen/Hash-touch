export const KdsSettingsProvider = ({ children }: { children: ReactNode }) => {
  const [settings, setSettings] = useState<ThemeSettings>(defaultSettings);
  const [_, setIsLoaded] = useState(false);

  // Load settings from SQLite on mount
  useEffect(() => {
    const loadSettings = async () => {
      try {
        // Try to migrate from localStorage if exists
        const oldSettings = localStorage.getItem("kds_settings");
        if (oldSettings) {
          console.log("[KDS Settings] Migrating from localStorage to SQLite");
          const parsed = JSON.parse(oldSettings);
          await kdsSettingsLocal.saveSettings(parsed);
          localStorage.removeItem("kds_settings");
          setSettings(parsed);
        } else {
          // Load from SQLite
          const saved = await kdsSettingsLocal.getSettings();
          setSettings(saved);
        }
      } catch (error) {
        console.error("[KDS Settings] Failed to load settings:", error);
        setSettings(defaultSettings);
      } finally {
        setIsLoaded(true);
      }
    };

    loadSettings();
  }, []);

  const updateSettings = async (newSettings: Partial<ThemeSettings>) => {
    const updated = { ...settings, ...newSettings };
    setSettings(updated);

    // Save to SQLite
    try {
      await kdsSettingsLocal.saveSettings(updated);
    } catch (error) {
      console.error("[KDS Settings] Failed to save settings:", error);
    }
  };

  const resetSettings = async () => {
    setSettings(defaultSettings);
    try {
      await kdsSettingsLocal.resetSettings();
    } catch (error) {
      console.error("[KDS Settings] Failed to reset settings:", error);
    }
  };

  return (
    <KdsSettingsContext.Provider value={{ settings, updateSettings, resetSettings }}>
      {children}
    </KdsSettingsContext.Provider>
  );
};

// eslint-disable-next-line react-refresh/only-export-components
export const useKdsSettings = () => {
  const context = useContext(KdsSettingsContext);
  if (!context) {
    throw new Error("useKdsSettings must be used within KdsSettingsProvider");
  }
  return context;
};
